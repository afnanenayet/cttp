/* http_status.h    Afnan Enayet
 *
 * Defines constants defining HTTP status response codes.
 *
 * ---------------------------------------------------------------------------
 * Codes:
 *
 * OK: 200
 * Resource not found: 404
 * Resource moved permanently: 303
 * Resource moved temporarily: 302
 * Server error: 500
 */

#ifndef _http_status_codes_h
#define _http_status_codes_h

#define HTTP_OK 200
#define HTTP_NOT_FOUND 404
#define HTTP_MOVED_PERMANENTLY 303
#define HTTP_MOVED_TEMPORARILY 302
#define HTTP_SERVER_ERROR 500

#endif // _http_status_codes_h
